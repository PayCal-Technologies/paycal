<article id="accessibility" class="article doc-article" aria-label="__POLICIES_ACCESSIBILITY_ARIA__">
  <header class="doc-article-header">
    <h2>__POLICIES_ACCESSIBILITY_TITLE__</h2>
    <p class="deck">__POLICIES_ACCESSIBILITY_DECK__</p>
  </header>
  <section class="doc-article-body">
    <section class="doc-section">
      <p>We want PayCal to work for people using keyboards, touch, screen readers, zoom, and other assistive tools.</p>

      <p>Accessibility is part of how we build the product. We work on clear labels, predictable navigation, usable focus states, and layouts that do not depend on precise mouse use.</p>

      <h3>1. What this policy means</h3>

      <p>We use WCAG 2.1 Level AA as our working standard and treat accessibility as an ongoing engineering responsibility, not a one-time checklist.</p>

      <p>We do not claim perfect conformance across every screen at all times. We do commit to improving verified issues, documenting meaningful changes, and keeping accessibility work visible.</p>

      <h3>2. What users should expect</h3>

      <ul>
        <li>Keyboard access for core workflows, including safe shortcut behavior that does not interrupt typing.</li>
        <li>Clear headings, labels, landmarks, and feedback messages on audited routes.</li>
        <li>More than one way to navigate key public content where that improves orientation.</li>
        <li>A first-party path for reporting accessibility barriers without hunting for the right contact channel.</li>
      </ul>

      <h3>3. Learn more</h3>

      <p>For current implementation notes, recent fixes, and verification scope, see <a href="/transparency/accessibility/">Accessibility Transparency</a>.</p>

      <h3>4. Feedback</h3>

      <p>If something is hard to use or inaccessible, use the <a href="/transparency/accessibility/">Accessibility Transparency</a> feedback form or email <a href="mailto:info@paycal.app">info@paycal.app</a>. We review accessibility feedback and use it to guide fixes and priorities.</p>
    </section>
  </section>
