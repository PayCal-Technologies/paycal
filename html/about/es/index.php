      <h2>En qué ayuda</h2>
      <ul class="doc-fact-list">
        <li>Registrar horas regulares, horas extra, traslados y asignaciones</li>
        <li>Ver ingresos y deducciones estimadas antes del pago</li>
        <li>Seguir períodos de pago con vista de calendario</li>
        <li>Gestionar trabajo en múltiples sitios</li>
      </ul>

      <h2>Funciones clave</h2>
      <ul class="doc-fact-list">
        <li><strong>Entrada basada en calendario:</strong> Registra y revisa trabajo día por día</li>
        <li><strong>Seguimiento multisede:</strong> Mantén registros ordenados entre ubicaciones</li>
        <li><strong>Vista previa de períodos:</strong> Ve cómo los registros afectan tu ciclo actual</li>
        <li><strong>Cálculos consistentes:</strong> Estimaciones confiables de deducciones y pago neto</li>
        <li><strong>Opciones de exportación:</strong> Descarga registros en PDF, CSV o TXT</li>
        <li><strong>Navegación sencilla:</strong> Funciona con teclado, toque o ratón</li>
        <li><strong>Desglose claro:</strong> Vista simple de ingresos y deducciones</li>
      </ul>

      <h2>Para quién es</h2>
      <ul class="doc-fact-list">
