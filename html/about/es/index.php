      </ul>

      <h2>Seguridad y privacidad</h2>
      <p>PayCal está diseñada para mantener tus datos privados y seguros.</p>
      <ul class="doc-fact-list">
        <li>Los datos sensibles se cifran</li>
        <li>Los datos se almacenan de forma segura y con exposición mínima</li>
        <li>Las sesiones limpian información sensible cuando cambia el contexto</li>
        <li>Los identificadores personales se limitan siempre que sea posible</li>
