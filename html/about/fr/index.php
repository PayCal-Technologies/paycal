      <h2>Ce que PayCal aide à faire</h2>
      <ul class="doc-fact-list">
        <li>Suivre les heures régulières, heures supplémentaires, déplacements et indemnités</li>
        <li>Voir les revenus et retenues estimés avant le paiement</li>
        <li>Suivre les périodes de paie avec une vue calendrier</li>
        <li>Gérer le travail sur plusieurs sites</li>
      </ul>

      <h2>Fonctionnalités clés</h2>
      <ul class="doc-fact-list">
        <li><strong>Saisie basée calendrier :</strong> Enregistrez et révisez le travail jour par jour</li>
        <li><strong>Suivi multi-sites :</strong> Gardez les dossiers organisés entre plusieurs lieux</li>
        <li><strong>Aperçus de période :</strong> Voyez l'impact des entrées sur votre cycle en cours</li>
        <li><strong>Calculs cohérents :</strong> Estimations fiables des retenues et du net</li>
        <li><strong>Options d'export :</strong> Téléchargez vos dossiers en PDF, CSV ou TXT</li>
        <li><strong>Navigation simple :</strong> Fonctionne au clavier, au toucher ou à la souris</li>
        <li><strong>Vue claire :</strong> Présentation simple des revenus et retenues</li>
      </ul>

      <h2>Pour qui</h2>
      <ul class="doc-fact-list">
        <li><strong>Particuliers :</strong> Suivez gratuitement votre travail et votre historique de paie</li>
        <li><strong>Équipes :</strong> Utilisez Premium pour l'accès partagé et la coordination</li>
      </ul>

      <h2>Sécurité et confidentialité</h2>
      <p>PayCal est conçu pour garder vos données privées et sécurisées.</p>
      <ul class="doc-fact-list">
        <li>Les données sensibles sont chiffrées</li>
        <li>Les données sont stockées de manière sécurisée avec une exposition minimale</li>
        <li>Les sessions effacent les informations sensibles lors des changements de contexte</li>
        <li>Les identifiants personnels sont limités autant que possible</li>
      </ul>

      <h2>Accessibilité</h2>
      <p>L'accessibilité est intégrée par défaut.</p>
      <ul class="doc-fact-list">
        <li>Compatible avec les lecteurs d'écran et la navigation clavier</li>
        <li>Options typographiques lisibles, dont des styles adaptés à la dyslexie</li>
        <li>Contraste élevé et clarté visuelle</li>
      </ul>

