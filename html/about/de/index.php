      <h2>Wobei PayCal hilft</h2>
      <ul class="doc-fact-list">
        <li>Erfassung von regulären Stunden, Überstunden, Reisezeit und Zulagen</li>
        <li>Geschätzte Einnahmen und Abzüge vor dem Zahltag sehen</li>
        <li>Abrechnungszeiträume in einer Kalenderansicht verfolgen</li>
        <li>Arbeit über mehrere Einsatzorte hinweg verwalten</li>
      </ul>

      <h2>Wichtige Funktionen</h2>
      <ul class="doc-fact-list">
        <li><strong>Kalenderbasierte Eingabe:</strong> Arbeit Tag für Tag erfassen und prüfen</li>
        <li><strong>Multi-Standort-Erfassung:</strong> Datensätze über mehrere Orte hinweg geordnet halten</li>
        <li><strong>Abrechnungs-Vorschau:</strong> Sehen, wie Einträge den aktuellen Zyklus beeinflussen</li>
        <li><strong>Konsistente Berechnungen:</strong> Verlässliche Schätzungen für Abzüge und Nettolohn</li>
        <li><strong>Exportoptionen:</strong> Datensätze als PDF, CSV oder TXT herunterladen</li>
        <li><strong>Einfache Navigation:</strong> Funktioniert mit Tastatur, Touch oder Maus</li>
        <li><strong>Klare Aufschlüsselungen:</strong> Verständliche Übersicht zu Einnahmen und Abzügen</li>
      </ul>

      <h2>Für wen es ist</h2>
      <ul class="doc-fact-list">
        <li><strong>Einzelpersonen:</strong> Arbeit und Gehaltshistorie kostenlos erfassen</li>
        <li><strong>Teams:</strong> Premium für gemeinsamen Zugriff und Koordination nutzen</li>
      </ul>

      <h2>Sicherheit und Datenschutz</h2>
      <p>PayCal wurde entwickelt, um Daten privat und sicher zu halten.</p>
      <ul class="doc-fact-list">
        <li>Sensible Daten werden verschlüsselt</li>
        <li>Daten werden sicher gespeichert und mit minimaler Exposition verarbeitet</li>
        <li>Sitzungen löschen sensible Informationen bei Kontextwechseln</li>
        <li>Personenbezogene Identifikatoren werden soweit möglich begrenzt</li>
